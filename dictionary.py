'''Dictionary is a data structure in python that holds key:value pair. Key-value is provided in the dictionary 
to make it more optimized. '''

# A simple dictionary in pyhon.
python_Book = { "author" : "Eric Matthes", "Title":"Python Crash Course","Edition":"2nd Edition"}
print(python_Book)

# Using the dictionary with f-string
print(f"The author of {python_Book['Title']} is {python_Book['author']}")

